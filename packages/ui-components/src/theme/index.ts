/**
 * Create a theme based on the specified mode
 * @param mode Theme mode (light, dark, or system)
 * @returns MUI theme
 */
export function createAppTheme(mode: PaletteMode): ThemeOptions {
  return createTheme({
    ...commonOptions,
    palette: mode === 'light' ? lightPalette : darkPalette,
  });
}

/**
 * Get the theme mode based on system preference
 * @param themeMode User's theme preference
 * @returns The effective theme mode (light or dark)
 */
export function getEffectiveThemeMode(themeMode: ThemeMode): PaletteMode {
  if (themeMode === 'system') {
    // Check system preference
    if (typeof window !== 'undefined') {
      return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
    }
    return 'light'; // Default to light if window is not available (SSR)
  }
  return themeMode;
}

